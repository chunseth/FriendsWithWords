const INVALID_SHORT_WORDS = new Set(["dbe"]);

// Irregular plurals that don't follow -s/-es rules (e.g. woman→women, tooth→teeth).
const VALID_IRREGULAR_PLURALS = new Set([
  "children",
  "feet",
  "geese",
  "men",
  "mice",
  "oxen",
  "people",
  "teeth",
  "women",
]);

// Irregular adverbs (e.g. good→well); same-form adj/adv like fast/hard are already in dictionary.
const VALID_IRREGULAR_ADVERBS = new Set([
  "well",   // good → well
]);

// Irregular comparatives and superlatives (good→better/best, bad→worse/worst, etc.).
const VALID_IRREGULAR_ADJECTIVE_FORMS = new Set([
  "best",      // good → better, best
  "better",
  "worst",     // bad → worse, worst
  "worse",
  "farther",   // far → farther/further, farthest/furthest
  "farthest",
  "further",
  "furthest",
  "least",     // little → less, least
  "less",
  "most",      // much/many → more, most
  "more",
  "eldest",    // old → elder, eldest (family)
  "elder",
  "joker",
]);

class Dictionary {
  constructor() {
    this.words = new Set();
    this.loaded = false;
  }

  async load() {
    if (this.loaded) return;

    dictionaryWords.forEach((word) => {
      if (word.length === 2 && !VALID_TWO_LETTER_WORDS.has(word)) {
        return;
      }

      if (word.length <= 3 && INVALID_SHORT_WORDS.has(word)) {
        return;
      }

      this.words.add(word);
    });

    VALID_TWO_LETTER_WORDS.forEach((word) => {
      this.words.add(word);
    });

    VALID_THREE_LETTER_WORDS.forEach((word) => {
      this.words.add(word);
    });

    VALID_CUSTOM_WORDS.forEach((word) => {
      this.words.add(word);
    });

    VALID_IRREGULAR_VERB_FORMS.forEach((word) => {
      this.words.add(word);
    });

    VALID_IRREGULAR_PLURALS.forEach((word) => {
      this.words.add(word);
    });

    VALID_IRREGULAR_ADVERBS.forEach((word) => {
      this.words.add(word);
    });

    VALID_IRREGULAR_ADJECTIVE_FORMS.forEach((word) => {
      this.words.add(word);
    });

    this.loaded = true;
    console.log(`Dictionary loaded: ${this.words.size} words`);
  }

  isValid(word) {
    if (!this.loaded) return false;
    const normalizedWord = word.toLowerCase();
    if (
      (normalizedWord.length <= 3 && INVALID_SHORT_WORDS.has(normalizedWord)) ||
      INVALID_IRREGULAR_INFLECTIONS.has(normalizedWord)
    ) {
      return false;
    }
    if (this.words.has(normalizedWord)) {
      return true;
    }

    return this.isLikelyInflectedForm(normalizedWord);
  }

  getWordCount() {
    return this.words.size;
  }

  hasKnownBaseForm(candidates, derivedWord = "", options = {}) {
    const { allowSecondSuffixOnEnEr = false } = options;
    return candidates.some((candidate) => {
      if (!candidate) return false;

      // Prevent double suffix chains like "evoker" + "ed" => "evokered"
      // unless the resulting word is explicitly allowlisted.
      if (
        (candidate.endsWith("en") || candidate.endsWith("er")) &&
        !allowSecondSuffixOnEnEr &&
        !VALID_SECOND_SUFFIX_ON_EN_ER.has(derivedWord)
      ) {
        return false;
      }

      return this.words.has(candidate);
    });
  }

  isLikelyInflectedForm(word) {
    if (word.length < 4) {
      return false;
    }

    if (word.endsWith("ies") && word.length > 4) {
      return this.hasKnownBaseForm([`${word.slice(0, -3)}y`], word);
    }

    if (
      word.endsWith("es") &&
      /(s|x|z|ch|sh|o)es$/.test(word) &&
      word.length > 4
    ) {
      // Try both: box->boxes (drop -es) and amaze->amazes (drop -s only)
      return this.hasKnownBaseForm(
        [word.slice(0, -2), word.slice(0, -1)],
        word
      );
    }

    // Plurals of nouns ending in f/fe: life→lives, calf→calves, wolf→wolves, knife→knives, wife→wives
    if (word.endsWith("ves") && word.length > 4) {
      const base = word.slice(0, -3);
      return this.hasKnownBaseForm([`${base}f`, `${base}fe`], word);
    }

    if (word.endsWith("s") && !word.endsWith("ss")) {
      return this.hasKnownBaseForm([word.slice(0, -1)], word);
    }

    if (word.endsWith("ing") && word.length > 5) {
      const base = word.slice(0, -3);
      return this.hasKnownBaseForm([
        base,
        `${base}e`,
        base.endsWith("y") ? `${base.slice(0, -1)}ie` : "",
        this.removeDoubledTrailingConsonant(base),
      ], word);
    }

    if (word.endsWith("ied") && word.length > 4) {
      return this.hasKnownBaseForm([`${word.slice(0, -3)}y`], word);
    }

    if (word.endsWith("ed") && word.length > 4) {
      const base = word.slice(0, -2);
      return this.hasKnownBaseForm([
        base,
        `${base}e`,
        this.removeDoubledTrailingConsonant(base),
      ], word);
    }

    if (word.endsWith("en") && word.length > 4) {
      const base = word.slice(0, -2);
      return this.hasKnownBaseForm([
        base,
        `${base}e`,
        this.removeDoubledTrailingConsonant(base),
      ], word);
    }

    if (word.endsWith("ier") && word.length > 4) {
      return this.hasKnownBaseForm([`${word.slice(0, -3)}y`], word);
    }

    if (word.endsWith("er") && word.length > 4) {
      const base = word.slice(0, -2);
      // Comparative forms ending in y should use -ier (happy -> happier),
      // not y + er (happyer).
      if (base.endsWith("y")) {
        const hasVowelBeforeY = /[aeiou]y$/.test(base);
        if (
          !hasVowelBeforeY &&
          !VALID_Y_ER_SUFFIX_EXCEPTIONS.has(word)
        ) {
          return false;
        }
      }
      return this.hasKnownBaseForm([
        base,
        `${base}e`,
        this.removeDoubledTrailingConsonant(base),
      ], word);
    }

    // Adverbs: adjective + -ly (quickly, gently, happily, nationally, economically)
    if (word.endsWith("ly") && word.length > 4) {
      const withoutLy = word.slice(0, -2);
      const candidates = [withoutLy, withoutLy + "e"]; // quick→quickly, gentle→gently
      if (word.endsWith("ily")) {
        candidates.push(word.slice(0, -3) + "y"); // happy→happily
      }
      if (word.endsWith("ically")) {
        candidates.push(word.slice(0, -4)); // basic→basically
        candidates.push(word.slice(0, -4) + "ic"); // economic→economically
      }
      if (word.endsWith("ally") && !word.endsWith("ically")) {
        candidates.push(word.slice(0, -2)); // national→nationally
      }
      return this.hasKnownBaseForm(candidates, word, {
        allowSecondSuffixOnEnEr: true,
      });
    }

    return false;
  }

  removeDoubledTrailingConsonant(base) {
    if (base.length < 3) {
      return "";
    }

    const lastChar = base[base.length - 1];
    const secondLastChar = base[base.length - 2];
    if (lastChar === secondLastChar && !/[aeiou]/.test(lastChar)) {
      return base.slice(0, -1);
    }

    return "";
  }
}

